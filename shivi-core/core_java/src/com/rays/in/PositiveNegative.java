package com.rays.in;

public class PositiveNegative {

	public static void main(String[] args) {
		
		int num = -20;
		
		if ( num > 0) {
			System.out.println(num + " is positive number.");
		} else if ( num < 0) {
			System.out.println(num + " is negative number.");
		} else {
			System.out.println(num + " its zero.");
		}
	}
}
