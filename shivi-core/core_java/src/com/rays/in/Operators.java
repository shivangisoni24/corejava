	    /**
	     * Less than operator ('<') checks if the left operand is less than the right operand.
	     * it returns 'true' if the condition is met. For example:
         * 
	     * <pre>
	     * if (x < 10) {
	     *     // code to execute if x is less than 10
	     * }
	     * </pre>    
	     */
            
